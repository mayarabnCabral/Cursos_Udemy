"""
int -> Números inteiros
     -> Representa qualquer número sem casas decimais, positivos ou negativos
"""
print(5)   # int positivo 
print(-5)  # int negativo 
print(0)
"""
float -> Números com casas decimais
       -> Representa qualquer número com casas decimais, positivos ou negativos
       -> Utiliza-se o ponto (.) para separar a parte decimal
"""
print(5.5)   # float positivo 
print(-5.5)  # float negativo 
print(0.0)

print(type(15.5)) # type informa qual é o tipo do dado
print(type('Mayara Bueno Nunes Cabral'))
print(type(0))
print(type(15), type('Mayara'), type(1.1), type(True))